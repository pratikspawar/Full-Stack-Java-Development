package kbp.day_1._Datatype;

public class Datatype {

public static void main(String[] args) {
	byte a=10;
	byte b=a;
	int c=a;
	short e=a;
	System.out.println("a="+a);
	int r=130;
	byte rt = (byte)r;
	System.out.println("r="+rt);
	float q=10.4f;
	double p=10;
	char m=11;
	boolean bz=true;
	System.out.println("m"+m);
}
}
