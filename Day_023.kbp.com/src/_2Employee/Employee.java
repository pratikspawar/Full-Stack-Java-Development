package _2Employee;

public class Employee {
private String name;
private int eid;
private double salary;
private int age;
private int exp;
public String getName() {
	return name;
}
public void setName(String string) {
	this.name = string;
}
public int getEid() {
	return eid;
}
public void setEid(int eid) {
	this.eid = eid;
}
public double getSalary() {
	return salary;
}
public void setSalary(double salary) {
	this.salary = salary;
}
public int getAge() {
	return age;
}
public void setAge(int age) {
	this.age = age;
}
public int getExp() {
	return exp;
}
public void setExp(int exp) {
	this.exp = exp;
}

}
